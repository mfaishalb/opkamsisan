                    <h2>Jejak Aktif vs. Pasif</h2>
                    <p>Jejak digital Anda terdiri dari dua jenis:</p>
                    <ul>
                        <li><strong>Jejak Aktif:</strong> Data yang sengaja Anda bagikan. Contohnya termasuk status di media sosial, komentar di blog, atau foto yang Anda unggah. Anda memiliki kendali penuh atas jejak ini.</li>
                        <li><strong>Jejak Pasif:</strong> Data yang dikumpulkan tanpa Anda sadari. Contohnya adalah riwayat pencarian Anda, alamat IP yang terekam saat mengunjungi situs web, atau data lokasi yang dikumpulkan oleh aplikasi.</li>
                    </ul>
                    <h2>Mengapa Ini Penting? "Internet Tidak Pernah Lupa"</h2>
                    <p>Data yang sudah masuk ke internet sangat sulit untuk dihapus sepenuhnya. Jejak digital Anda dapat memengaruhi banyak aspek kehidupan nyata, mulai dari reputasi pribadi hingga peluang karier. Perekrut kerja seringkali memeriksa media sosial kandidat untuk menilai kepribadian dan profesionalisme mereka.</p>
                    <blockquote><p>Sebuah postingan gegabah yang dibuat 10 tahun lalu bisa saja ditemukan hari ini dan menimbulkan masalah.</p></blockquote>
