                <section class="artikel-konten">
                    <p class="intro">Tanyakan pada diri Anda: jika laptop Anda dicuri atau hard drive Anda rusak HARI INI, data apa yang akan hilang selamanya? Jika jawabannya membuat Anda cemas, maka artikel ini untuk Anda. Mencadangkan data (backup) bukanlah pilihan, melainkan keharusan.</p>
                    <h2>Memperkenalkan Aturan Emas: Strategi 3-2-1</h2>
                    <p>Para profesional keamanan dan IT di seluruh dunia mengikuti sebuah strategi sederhana namun sangat kuat yang disebut Aturan 3-2-1. Aturan ini memastikan data Anda selamat dari hampir semua skenario bencana.</p>
                    <ol>
                        <li><strong>(3) Tiga Salinan Data:</strong> Simpan setidaknya **tiga** total salinan data penting Anda. Satu adalah data asli di perangkat utama Anda, dan dua lainnya adalah cadangan.</li>
                        <li><strong>(2) Dua Media Berbeda:</strong> Simpan salinan-salinan tersebut di setidaknya **dua** jenis media penyimpanan yang berbeda. Mengapa? Untuk melindungi dari kegagalan jenis media tertentu. Contoh: satu salinan di hard drive internal laptop, satu lagi di hard drive eksternal.</li>
                        <li><strong>(1) Satu Lokasi Terpisah (Off-site):</strong> Simpan setidaknya **satu** dari salinan cadangan tersebut di lokasi fisik yang berbeda. Mengapa? Untuk melindungi dari bencana lokal seperti kebakaran, banjir, atau pencurian.</li>
                    </ol>
                    <h2>Contoh Praktis Implementasi 3-2-1</h2>
                    <blockquote><p>Anda memiliki file skripsi yang sangat penting.
                    <br><strong>Salinan 1 (Asli):</strong> Ada di laptop Anda.
                    <br><strong>Salinan 2 (Media Berbeda):</strong> Setiap malam, Anda menyalinnya ke hard drive eksternal.
                    <br><strong>Salinan 3 (Lokasi Terpisah):</strong> Anda mengunggahnya ke layanan cloud seperti Google Drive atau Dropbox.
                    <br><br>Dengan skema ini, jika laptop Anda rusak, Anda punya data di hard drive eksternal. Jika rumah Anda kemalingan (laptop dan hard drive eksternal hilang), Anda masih punya data aman di cloud. Anda terlindungi.</p></blockquote>
                    <p>Strategi backup yang baik adalah satu-satunya jaring pengaman sejati melawan kehilangan data dan serangan ransomware.</p>
