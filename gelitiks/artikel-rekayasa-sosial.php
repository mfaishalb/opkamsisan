                <section class="artikel-konten">
                    <p class="intro">Ancaman siber terbesar seringkali bukanlah kode yang canggih, melainkan manipulasi psikologis yang lihai. Inilah dunia Rekayasa Sosial (Social Engineering), di mana targetnya bukanlah mesin, melainkan pikiran manusia.</p>
                    <h2>Apa Itu Rekayasa Sosial?</h2>
                    <p>Rekayasa sosial adalah seni menipu orang lain agar mereka secara sukarela melakukan tindakan yang merugikan keamanan diri sendiri atau organisasi mereka. Tindakan ini bisa berupa membocorkan informasi rahasia (seperti password), mentransfer uang, atau memberikan akses ke sistem terbatas.</p>
                    <blockquote><p>"Amatir meretas sistem, profesional meretas manusia." - Bruce Schneier</p></blockquote>
                    <h2>Pilar Psikologi yang Dimanfaatkan</h2>
                    <p>Serangan ini berhasil karena mengeksploitasi kecenderungan alami manusia. Beberapa pilar utamanya adalah:</p>
                    <ol>
                        <li><strong>Otoritas:</strong> Penipu menyamar sebagai figur yang memiliki kuasa, seperti atasan (CEO), polisi, atau petugas bank, membuat korban merasa wajib untuk patuh.</li>
                        <li><strong>Kepercayaan:</strong> Membangun hubungan palsu atau menyamar sebagai teman atau rekan kerja untuk mendapatkan kepercayaan korban.</li>
                        <li><strong>Rasa Urgensi & Ketakutan:</strong> Menciptakan skenario darurat seperti "Akun Anda akan diblokir dalam 1 jam!" atau "Ada tagihan mencurigakan!" untuk membuat korban panik dan tidak berpikir jernih.</li>
                        <li><strong>Keingintahuan & Keserakahan:</strong> Menggunakan umpan seperti "Lihat video memalukan Anda di sini!" atau "Selamat, Anda memenangkan hadiah!" untuk memancing korban agar mengklik link berbahaya.</li>
                    </ol>
                    <h2>Cara Bertahan</h2>
                    <ul>
                        <li><strong>Prinsip "Zero Trust":</strong> Jangan langsung percaya. Selalu asumsikan permintaan yang tidak biasa atau mendesak perlu diverifikasi.</li>
                        <li><strong>Verifikasi Lewat Saluran Berbeda:</strong> Mendapat email aneh dari atasan? Jangan balas emailnya. Telepon atau temui langsung atasan Anda untuk konfirmasi.</li>
                        <li><strong>Berpikir Sebelum Klik:</strong> Sebelum mengklik link atau membuka lampiran, tanyakan pada diri sendiri: "Apakah saya mengharapkan ini? Apakah ini masuk akal?".</li>
                    </ul>
